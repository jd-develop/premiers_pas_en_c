#include <stdio.h>

int main() {
    int a = 70;
    int b = 82;
    int z = 185;
    int d = 30;
    printf("%d\n", a);
    printf("%d\n", b);
    printf("%d\n", z);
    printf("%d\n\n", d);

    char c = 'c';
    char o = 'o';
    char u = 'u';
    char C = 'C';
    char O = 'O';
    char U = 'U';
    printf("%c%c%c%c%c%c\n", c, o, u, C, O, U);

    return 0;
}
