#include <iostream>
#include <cmath>
using namespace std;

int main() {
    // max and min (cmath not needed)
    cout << max(1, 4) << "\n";
    cout << min(1, 4) << "\n";

    // cmath functions
    cout << sqrt(4) << "\n";
    cout << pow(exp(1), log(2)) << "\n";
    cout << round(2.6) << "\n";

    return 0;
}
